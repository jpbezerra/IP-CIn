nome_bruxa = str(input())
winifred_sanderson = "Winifred Sanderson"
sarah_sanderson = "Sarah Sanderson"
mary_sanderson = "Mary Sanderson"

if nome_bruxa == winifred_sanderson:
    print("O livro!! Sem ele, a mais poderosa das bruxas não conseguirá realizar seus feitiços.")
elif nome_bruxa == sarah_sanderson:
    print("Tapem os ouvidos, sua voz pode hipnotizar!!")
elif nome_bruxa == mary_sanderson:
    print("Seu ótimo olfato e péssimo senso de direção podem ser usados para confundí-la.")
else:
    print("Só mais uma criança fantasiada, continuem atentos!")